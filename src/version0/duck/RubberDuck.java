package version0.duck;

// 小黄鸭
public class RubberDuck extends Duck{
    public void swim() {
        System.out.println("随波逐流");
    }

    public void quack() {
        System.out.println("不会叫，被捏的时候会发出吱吱的声音");
    }

    public void lookLike() {
        System.out.println("黄色外观，红色嘴巴，塑料材质");
    }
}
